export const translations = new Proxy(rawTranslations, {
  get(target, lang) {
    return new Proxy({}, {
      get(_, key) {
        if (typeof key === 'string') {
          if (i18n.isInitialized && i18n.exists(key)) {
            return i18n.t(key);
          }
        }
        return (target as any)[lang]?.[key] || (target as any)['en']?.[key] || key;
      }
    });
  }
});

export type Language = 'ta' | 'en' | 'hi' | 'te' | 'kn' | 'ml' | 'mr' | 'bn' | 'gu' | 'pa';

export function getLocalValue<T>(obj: any, lang: Language, fallbackKey: string = 'En'): T {
  if (!obj) return undefined as any;

  // 1. Check if the exact lang key exists in the object (e.g. obj.ta or obj['ta'])
  if (obj[lang] !== undefined) return obj[lang];

  // 2. Build suffix lookups like nameTa, splitInstructionsTa, etc.
  const capitalizedLang = lang.charAt(0).toUpperCase() + lang.slice(1);
  const targetKey = Object.keys(obj).find(k => k.endsWith(capitalizedLang));
  if (targetKey && obj[targetKey] !== undefined) {
    return obj[targetKey];
  }

  // 3. Fallback to Capitalized Fallback Key, e.g. 'En'
  const targetFallbackKey = Object.keys(obj).find(k => k.endsWith(fallbackKey));
  if (targetFallbackKey && obj[targetFallbackKey] !== undefined) {
    return obj[targetFallbackKey];
  }

  // 4. Fallback to 'en' or first found property
  if (obj['en'] !== undefined) return obj['en'];
  if (obj['ta'] !== undefined) return obj['ta'];

  const firstKey = Object.keys(obj)[0];
  return obj[firstKey];
}
